    /**
     * Method used to generate the slug from a last sentence or group of words.
     * @param string $text Text to generate the slug.
     * @return string
     */
    public function generateSlug($text)
    {
        $text = mb_convert_encoding((string)$text, self::DEFAULT_ENCODING, mb_list_encodings());

        $text = str_replace(array_keys($this->replacements), array_values($this->replacements), $text);

        if ($this->translate) {
            $text = str_replace(array_keys($this->charMap), $this->charMap, $text);
        }

        $text = preg_replace('/[^\p{L}\p{Nd}]+/u', $this->delimiter, $text);

        $text = preg_replace('/(' . preg_quote($this->delimiter, '/') . '){2,}/', '$1', $text);

        $text = trim($text, $this->delimiter);

        if ($this->limit) {
            $text = mb_substr($text, 0, $this->limit, self::DEFAULT_ENCODING);
        }

        if ($this->lowerCase) {
            $text = mb_strtolower($text, self::DEFAULT_ENCODING);
        }

        return $text;
    }

    /**
     * Method used to generate the slug from a last sentence or group of words,
     * validating in a database column if there is already some slug with the same value.
     *
     * @param string $text Text to generate slug.
     * @param string $tableName Table to search the database like a slug.
     * @param string $columnName Column to search the database like a slug.
     *
     * @return string
     */
    public function generateSigleSlug($text, $tableName, $columnName)
    {
        $slug = $this->generateSlug($text);

        $occurrencesSlug = DB::table($tableName)
            ->where($columnName, 'LIKE', "{$slug}%")
            ->count();

        if ($occurrencesSlug > 0) {
            $slug .= $this->delimiter . ($occurrencesSlug + 1);
        }

        return $slug;
    }

